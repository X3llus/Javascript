function checkIt(e) {
  if (e.target.style.textDecoration == "line-through") {
    e.target.style.textDecoration = "";
    updatePStyles(e.target);
  } else {
    e.target.style.textDecoration = "line-through";
  }
}

function makePriority(e) {
  if (e.className == "priority") {
    e.removeAttribute("class")
  } else {
    e.setAttribute("class", "priority");
  }
}

function updatePStyles(e) {
  if (e.className == "priority") {
    e.style.color = "red";
    e.style.textDecoration = "underline";
  } else {
    e.style.color = "black";
    e.style.textDecoration = "";
  }
}

function addToList(e) {
  let form = e.target.form;
  let node = document.createElement("li");
  let textNode = document.createTextNode(form[0].value);
  node.appendChild(textNode);
  node.addEventListener("click", checkIt)

  if (form[1].checked) {
    node.setAttribute("class", "priority");
  }

  document.querySelector("ul").appendChild(node)
  updatePStyles(node);
}

